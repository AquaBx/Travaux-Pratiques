def sun(n):
    """
        Exo 11
    """
    s = 0
    while n > 0:
        s += u(n)
        n-=1
    return s

def u(n):
    """
        Exo 11
    """
    u = 0
    while n>0:
        n-=1
        u = (6+u)/(6-u)
    return u